<?php 

/* Las constantes son similares a las variables con la diferencia de que no podemos volver a cambiar el valor de esa constante una vez asignado

Caracteristicas de las constantes:
1. Son variables globales: Podemos acceder a ellas desde cualquier parte del código (Scope o ámbito de las variables)
2. Por buena práctica definimos las constantes en mayúsculas
*/

define('PI', 3.1416);

echo PI;

?>